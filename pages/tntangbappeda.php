      <h2 class="heading">Visi Misi</h2>
      <p style="text-align: justify;"><strong>Tujuan Dan Sasaran Bappeda DIY</strong></p>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">Tujuan BAPPEDA DIY adalah “<strong><em>Menjadi Institusi Perencana yang Bernilai Tinggi</em>” </strong>dengan<strong>&nbsp;</strong>sasaran yang akan dicapai adalah “<strong><em>Terwujudnya perencanaan pembangunan daerah yang berkualitas</em></strong>”.Tujuan BAPPEDA DIY <em>menjadi</em> <em>Institusi Perencana yang Bernilai Tinggi</em> mempunyai makna bahwa BAPPEDA DIY harus mampu menjadi penggerak pembanguan daerah dalam rangka mencapai tujuan Pemerintah Daerah DIY, sehingga sasaran-sasaran yang telah ditetapkan Pemerintah Daerah melalui perencanaan yang didasarkan pertimbangan-pertimbangan rasional dalam menentukan strategi, arah kebijakan, dengan mempertimbangkan sumber daya yang dimiliki. Rasionalitas dan basis data adalah ciri yang menjadi kunci kualitas perencanaan, dalam arti perencanaan yang disusun&nbsp;merupakan perencanaan yang berdasarkan pertimbangan atas pengetahuan, informasi, dan data yang terkini (<em>evidence based</em> dan <em>knowledge based</em>) dengan mekanisme pelaksanaan (<em>delivery mechanism</em>) secara partisipatif dan visioner (berorientasi ke depan).</p>
<p style="text-align: justify;">Berkenaan dengan permasalahan dan pelaksanaan tugas-fungsi terkait serta menunjang program pemerintah daerah DIY dapat diuraikan sasaran perangkat daerah dan program perangkat daerah BAPPEDA DIY yang termuat dalam draft cascading RPJMD DIY 2017-2022 yaitu: “Terwujudnya Perencanaan Pembangunan Daerah yang Berkualitas”. Kualitas dimaksud ditunjukkan melalui ketercapaian sasaran-sasaran pembangunan daerah. Maka BAPPEDA DIY harus mampu menjadi instistusi perencanaan yang dapat menjamin perencanaan pembangunan yang dibuat dapat dilaksanakan dengan mempertimbangkan capaian sasaran yang telah ditetapkan.</p>    </div>
