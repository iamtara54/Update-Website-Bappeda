      <h2 class="heading">Sejarah Bappeda</h2>
      <p style="text-align: justify;">Demi meningkatkan keserasian pembangunan di daerah diperlukan adanya peningkatan keselarasan antara pembangunan sektoral dan pembangunan kewilayahan. Dalam upaya menjamin&nbsp; laju perkembangan, keseimbangan dan kesinambungan pembangunan daerah diperlukan perencanaan yang menyeluruh, terarah, terpadu, dan berkelanjutan.&nbsp; Manajemen perencanaan diperlukan untuk melakukan koordinasi perencanaan pembangunan daerah melalui aspek penguatan kelembagaan.&nbsp; Berdasarkan Keputusan Presiden Republik Indonesia Nomor 15 Tahun 1974 jo Kepmendagri Nomor 142 Tahun 1974 diamanatkan mengenai Pembentukan Badan Perencanaan Pembangunan Daerah sebagai badan koordinasi dalam perencanaan di daerah.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" src="img/tntngbappeda.png" alt="" width="535" height="269"></p>
<p style="text-align: justify;">Pada perkembangan selanjutnya, dalam rangka memantapkan kedudukan, tugas, dan fungsi Bappeda sebagai bagian dari organ yang membantu tugas Gubernur pada aspek perencanaan, diterbitkan Keputusan Presiden Republik Indonesia Nomor 27 Tahun 1980 tentang Pembentukan Badan Perencanaan Pembangunan Daerah, yang pelaksanaannya diatur melalui Peraturan Menteri Dalam Negeri Nomor 185 Tahun 1980 tentang Pedoman Organisasi dan Tata Kerja Badan Perencanaan Pembangunan Daerah Tingkat I dan Badan Perencanaan Pembangunan&nbsp; Daerah Tingkat II.&nbsp; Sebagaimana dalam Keppres tersebut, Badan Perencanaan Pembangunan Daerah di Provinsi Daerah Tingkat I disebut Bappeda Tingkat I, seperti halnya pada Pemerintah Provinsi/Tingkat I Daerah Istimewa Yogyakarta.&nbsp; Bappeda Tingkat I merupakan badan staf yang berada di bawah dan bertanggungjawab kepada Gubernur/Kepala Daerah Tingkat I.&nbsp; Susunan organisasi Bappeda Tingkat I terdiri dari : ketua, wakil ketua, sekretariat, bidang penelitian, bidang ekonomi, bidang sosial budaya, bidang fisik dan prasarana, bidang statistik dan laporan.</p>
<p>Sekilas Perkembangan Kelembagaan Bappeda DIY terangkum seperti berikut:</p>
<ul>
<li><strong>Masa 1974 – 1982</strong></li>
</ul>
<p style="margin-left: 1cm; text-align: justify;">Sebagaimana Keppres RI Nomor 15 Tahun 1974 jo Kepmendagri Nomor 142 Tahun 1974 tentang Pembentukan Badan Perencanaan Pembangunan Daerah, Pemerintah Provinsi Tingkat I Daerah Istimewa Yogyakarta (DIY) menindaklanjuti kebijakan tersebut dengan membentuk lembaga Bappeda melalui Keputusan Kepala Daerah Tingkat I DIY Nomor 341 Tahun 1974 tentang Pembentukan Badan Perencanaan Pembangunan Daerah Tingkat I DIY.&nbsp; Hal itu, dimaksudkan sebagai peleburan Keputusan Kepala Daerah Tingkat I DIY Nomor 325 Tahun 1972 tentang Pembentukan Badan Perencanaan Pembangunan Daerah Tingkat I DIY dan Keputusan Kepala Daerah Tingkat I DIY Nomor 43 Tahun 1973 tentang Perubahan/Penyempurnaan Keputusan Kepala Daerah Tingkat I DIY Nomor 325 Tahun 1972 tentang Pembentukan Badan Perencanaan Pembangunan Daerah Tingkat I DIY</p>
<ul>
<li style="text-align: justify;"><strong>Masa 1982 – 2001</strong></li>
</ul>
<p style="margin-left: 1cm; text-align: justify;">Perkembangan yang terjadi pada 1982, dikeluarkan Kepmendagri Nomor 362 Tahun 1977 dan mengacu pada pasal 107 ayat (1) dalam Kepmendagri Nomor 185 Tahun 1980, dibentuk Peraturan Daerah Provinsi DIY Nomor 1 Tahun 1982 tentang Pembentukan, Organisasi, dan Tata Kerja Bappeda Provinsi DIY.&nbsp; Melalui peraturan daerah tersebut Bappeda memiliki tugas membantu Kepala Daaerah dalam menentukan kebijaksanaan pada bidang perencanaan pembangunan daerah dan penilaian atas pelaksanaannya.&nbsp; Adapun susunan organisasi Bappeda saat itu, terdiri dari : ketua, wakil ketua, sekretariat, bidang-bidang (ekonomi, sosial budaya, fisik dan prasarana, statistik dan laporan, dan penelitian)</p>
<ul>
<li style="text-align: justify;"><strong>Masa 2001 – 2004</strong></li>
</ul>
<p style="margin-left: 1cm; text-align: justify;">Sejak tahun 2001, telah disahkan Peraturan Daerah Provinsi DIY Nomor 4 Tahun 2001 tentang Pembentukan dan Organisasi Lembaga Teknis Daerah pada lingkungan Pemerintah Provinsi DIY.&nbsp; Peraturan daerah tersebut ditindaklanjuti melalui Keputusan Gubernur DIY Nomor 93 Tahun 2001 tentang Uraian Tugas dan Tata Kerja Bappeda Provinsi DIY, yang salah satunya menyatakan bahwa susunan organisasi Bappeda Provinsi DIY terdiri dari : kepala badan, sekretariat, bidang pemerintahan, bidang perekonomian, bidang kesejahteraan rakyat, bidang fisik prasarana, bidang penelitian &amp; pengendalian, dan kelompok jabatan fungsional. Untuk struktur sekretariat/bidang, masing-masing dipimpin oleh seorang sekretaris/kepala bidang.&nbsp; Sedangkan kelompok jabatan fungsional berkedudukan di bawah dan bertanggungjawab langsung kepada kepala badan.</p>
<ul>
<li style="text-align: justify;"><strong>Masa 2004 – 2008</strong></li>
</ul>
<p style="margin-left: 1cm; text-align: justify;">Sejak diterbitkannya Peraturan Daerah Provinsi DIY Nomor 2 Tahun 2004 tentang Pembentukan dan Organisasi Lembaga Teknis Daerah di Lingkungan Pemerintah Provinsi DIY, nama Badan Perencanaan Pembangunan Daerah (Bappeda) menjadi Badan Perencanaan Daerah (Bapeda). Bapeda merupakan unsur pelaksana pemerintah daerah pada bidang perencanaan daerah yang dipimpin oleh seorang kepala yang berkedudukan di bawah dan bertanggungjawab kepada Gubernur melalui Sekretaris Daerah.&nbsp; Menurut pasal 6 ayat (2), kelembagaan Bapeda terdiri atas : secretariat, bidang perencanaan wilayah, bidang investasi dan pemasaran potensi wilayah, bidang perekonomian dan penguatan potensi masyarakat, bidang daya saing dan kemandirian masyarakat, bidang pengendalian, unit pelaksana teknis, dan kelompok jabatan fungsional.</p>
<ul>
<li><strong>Masa 2008 – sekarang</strong></li>
</ul>
<p style="margin-left: 40px; text-align: justify;">Penataan kelembagaan dilakukan kembali dengan diterbitkannya Peraturan Daerah Provinsi DIY Nomor 7 Tahun 2008 tentang Organisasi dan Tata Kerja : Inspektorat, Badan Perencanaan Pembangunan Daerah, Lembaga Teknis Daerah, dan Satuan Polisi Pamong Praja Provinsi DIY.&nbsp; Produk hukum tersebut ditindaklajuti dengan keluarnya Peraturan Gubernur Provinsi DIY Nomor 52 Tahun 2008 tentang Rincian Tugas dan Fungsi Badan Perencanaan Pembangunan Daerah Provinsi DIY, yang selanjutnya diubah melalui Peraturan Gubernur Provinsi DIY Nomor 48 Tahun 2010 tentang Perubahan Peraturan Gubernur Provinsi DIY Nomor 52 Tahun 2008 tentang Rincian Tugas dan Fungsi Badan Perencanaan Pembangunan Daerah Provinsi DIY Nomor 52 Tahun 2008.&nbsp; Sejak saat itu, istilah “Bapeda” kembali menjadi “Bappeda”, dengan susunan organisasi (pasal 10 ayat (2)) sebagai berikut : sekretariat, bidang pemerintahan, bidang perekonomian, bidang kesejahteraan rakyat, bidang sarana dan prasarana, bidang pengendalian, bidang perencanaan dan statistik, UPT Lembaga Teknis Daerah, dan kelompok jabatan fungsional.</p>   </div>
