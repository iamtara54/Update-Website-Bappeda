      <h3>PROFIL Litbang KOMWIL DIY</h3>
      <hr>
      <p style="text-align: justify;"><strong>Latar Belakang:</strong></p>
<p style="text-align: justify;"><strong><img style="float: left; border-width: 10px;" src="/view_image/images/1510306199_9478b1a568e0be99f1548285c04b88ff.png" width="142" height="150"></strong>AP2I merupakan organisasi profesi perencana pembangunan, didirikan dalam rangka memenuhi amanat penjelasan Pasal 3 Huruf b, PP RI Nomor 16 tahun 1994 tentang Jabatan Fungsional Pegawai Negeri Sipil, yang kemudian disempurnakan melalui PP Nomor 40 tahun 2010 tentang Perubahan atas PP Nomor 16 Tahun 1994 tentang Jabatan Fungsional Pegawai Negeri Sipil. AP2I berdiri pada <strong>tanggal 18 Juni 2007 </strong>melalui <strong>Akte Notaris Bonar Sihombing, SH. Nomor 17, dengan NPWP: 02.584.633.88-071.000</strong>.</p>
<p style="text-align: justify;">Anggota AP2I terdiri atas Para Pejabat Fungsional Perencana (PFP) sebagai Anggota Biasa, para Pejabat Struktural Non Perencana/Akademisi/Masyarakat Umum (Anggota Kehormatan), dan PFP yang diberhentikan sementara dari JFP dan ditugaskan dalam jabatan lain (Anggota Luar Biasa). Hak dan kewajiban masing-masing anggota dapat dilihat dalam AD-ART AP2I yang tersedia di website AP2I: <a href="http://ap2i-nasional.or.id/"><u>http://ap2i-nasional.or.id/</u></a></p>
<p style="text-align: justify;">Keberadaan AP2I juga merupakan amanat Peraturan Pemerintah Nomor 11 Tahun 2017 tentang Manajemen PNS, yang di dalamnya dimandatkan bahwa: <strong>(1) Setiap Jabatan Fungsional (JF) yang telah ditetapkan wajib memiliki 1 (satu) organisasi profesi Jabatan Fungsional dalam jangka waktu paling lama 5 (lima) tahun terhitung sejak tanggal penetapan JF, dan (2) Setiap pejabat fungsional wajib menjadi anggota organisasi profesi JF (yang didudukinya)... (Pasal 101 ayat (1) dan (2)).</strong></p>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;"><strong>Visi:<br></strong>“Menjadi &nbsp;Organisasi &nbsp;Profesi &nbsp;yang &nbsp;Kredibel, &nbsp;Mandiri, &nbsp;dan &nbsp;Berdayaguna &nbsp;bagi &nbsp;Anggota &nbsp;dan Lembaga Perencana Pusat dan Daerah”<br><br></p>
<p style="text-align: justify;"><strong>Misi:<br></strong>1. Meningkatkan profesionalitas perencana pusat dan daerah;<br>2. Mengembangkan jasa layanan yang berkualitas;<br>3. Membangun spirit “<em>sense of belonging</em>” anggota asosiasi;<br>4. Merevitalisasi peran dan fungsi AP2I;<br>5. Mengembangkan media &nbsp;komunikasi, &nbsp;informasi, &nbsp;dan &nbsp;jaringan &nbsp;kerjasama &nbsp;dengan para pemangku kepentingan perencanaan pusat dan daerah.<br><br></p>
<p style="text-align: justify;"><strong>Tujuan:<br></strong>AP2I merupakan satu-satunya organisasi Profesi Perencana Pemerintah di Indonesia yang berazaskan kepada Pancasila dan berlandaskan kepada Konstitusi &nbsp;Negara &nbsp;Kesatuan Republik Indonesia, didirikan dengan tujuan untuk:<br>1. Meningkatkan kemampuan, profesionalitas dan produktivitas perencana;<br>2. Meningkatkan kapasitas dan produktivitas instansi/unit perencana;<br>3. Menerapkan kode etik perencana;<br>4. Mengembangkan jejaring &nbsp;kerjasama &nbsp;antar-anggota Asosiasi Perencana Pemerintah Indonesia<br><br></p>
<p style="text-align: justify;"><strong>Dewan Pembina AP2I:<br></strong>1. Menteri Perencanaan Pembangunan Nasional/Kepala Badan Perencanaan Pembangunan Nasional), Ketua Dewan Pembina;<br>2. Sekretaris Menteri Perencanaan Pembangunan Nasional/Sestama Bappenas, Anggota Dewan Pembina;<br>3. Kepala Pusbindiklatren-Bappenas, Anggota Dewan Pembina AP2I.<br><br></p>
<p style="text-align: justify;"><strong>Dewan Penasehat AP2I:<br></strong>1. Dr. Ir. Roni Dwi Susanto, MSi, Deputi PEPP - Bappenas, (Ketua);<br>2. Dr. Ir. Leonard VH Tampubolon, MA, Deputi Bidang Ekonomi-Bappenas, (Anggota);<br>3. Staf Khusus Menteri PPN/Kepala Bappenas, (Anggota);<br>4. Dr. Ir. Oswar Muadzin Mungkasa, MURP, Deputi Gubernur Provinsi DKI Bidang Tata Ruang &amp; Lingkungan Hidup (Anggota);<br>5. Dr. Ir. Subandi, MSc, Deputi Kepala Bappenas Bidang Pembangunan Manusia, Masyarakat, dan Kebudayaan (Anggota).<br><br></p>
<p style="text-align: justify;"><strong>Pengurus Nasional AP2I (<em>Inti</em>):<br></strong>1. Dr. Haryanto, SE, MA -Bappenas (Ketua Umum);<br>2. Hening Surya Bayu Anggoro, ST, M.Sc.-Kementerian ESDM (Sekretaris Jenderal);<br>3. Surinta B. Sembiring, S.Sos., MM -LIPI (Bendahara Umum). (Daftar Pengurus Selengkapnya tedapat dalam SK)</p>         
