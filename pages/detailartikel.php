						<h3>KONSULTASI PUBLIK RKPD DIY 2020</h3>
						<p>Pemerintah Daerah Daerah Istimewa Yogyakarta (Pemda DIY) menyelenggarakan Konsultasi Publik dalam Rangka Penyusunan Rencana Kerja Pemerintah Daerah DIY 2020 (RKPD DIY 2020) yang diselenggarakan di Ruang Radyo Suyoso Kompleks Kepatihan Danurejan Yogyakarta, pada hari Senin, 11 Februari 2019. Konsultasi Publik dibuka dengan Arahan Sekretaris Daerah DIY, Ir. Gatot Saptadi. Sekretaris Daerah DIY menyampaikan "Daerah Istimewa Yogyakarta masih memiliki pekerjaan rumah berkenaan permasalahan pokok (1) Ketimpangan Antar Wilayah ; (2) Kemiskinan dan Ketimpangan Pendapatan; (3) Dukungan Pembangunan Bandara NYIA; (4) Keterbatasan Kemampuan Pembiayaan Pembangunan Daerah yang akan menjadi fokus pada tahun perencanaan 2020." 

						Setelah dibuka oleh Sekretaris Daerah, para narasumber utama Kepala Bappeda DIY, Budi Wibowo, SH. MM., Kepala BPS DIY Johanes De Britto Priyono, M.Sc., Wakil Ketua DPRD DIY Dharma Setiawan, MBA., menyampaikan materi dengan moderator Drs. Tri Mulyono, M.M. Staf Ahli Gubernur DIY Bidang Sosial, Budaya dan Kemasyarakatan. Kepala Bappeda DIY menyampaikan bahwa "Tema RKPD DIY 2020 ialah Percepatan Pembangunan Infastruktur dan Peningkatan SDM untuk Pertumbuhan Berkualitas dengan prioritas (1) Pembangunan Manusia dan Pengentasan Kemiskinan Berkelanjutan; (2) Penguatan Konektivitas dan Pemerataan Wilayah; (3) Aktivitas Perekonomian yang Berkelanjutan; (4) Pendukung Prioritas Pembangunan." Kepala Bappeda DIY menekankan Perangkat Daerah untuk memperhatikan sasaran RPJMD DIY dan target indikator yang terkait dengan Prioritas tema RKPD DIY tersebut terutama terkait prioritas untuk pengentasan kemiskinan yang pada akhir RPJMD DIY (2022) ditargetkan menjadi 7%. Berkenaan dengan kemiskinan, Kepala BPS DIY mengingatkan agar semua stakeholder mencermati data kemiskinan dari sudut pandang makro (BPS) dan mikro (PPLS/PBDT/BDTPPFM- Kemensos) agar intervensi dapat tepat sasaran. "Kami perlu mengingatkan kesalahan sasaran penyaluran bantuan (intervensi), karena mereka yang berada di 4 desil terrendah justru tidak menerima bantuan. Kalau kita masih bussiness as usual menurunkan kemiskinan sampai 7% (2022) akan sulit. Exit strategy penanggulangan kemiskinan di DIY (1) intervensi dari garis kemiskinan makanan (2) cermati Garis Kemiskinan per Kapita-Garis Kemiskinan per Rumah Tangga Miskin-Upah Minimum Regional; (3) benahi inclusion/ exclusion error," lanjut Kepala BPS DIY.</p>
						<br>
						<p>Wakil Ketua DPRD DIY, Dharma Setiawan, MBA menyampaikan pokok-pokok Pikiran-Pikiran DPRD yang untuk mewujudkan aspirasi masyarakat khususnya konstituen. "Investasi penting untuk mendorong pengurangan kemiskinan, diantaranya terkait investasi untuk NYIA (aerotropolis dst) untuk mendongkrak kondisi makro (kemiskinan). Isu strategis pembangunan 2020 yang diidentifikasi oleh DPRD DIY antara lain terkait angka kemiskinan, kesenjangan antar kelompok pendapatan (indeks gini) dan ketimpangan pembangunan infrastruktur wilayah, khususnya kota-desa," ungkap Wakil Ketua DPRD DIY, Dharma Setiawan. Pokok-pokok pikiran DPRD DIY telah dientry dalam aplikasi jogjaplan (e-pokir), sambung Wakil Ketua DPRD DIY. 

						Seusai paparan narasumber dilaksanan diskusi interaktif yang dimoderatori oleh  Drs. Tri Mulyono, M.M. Staf Ahli Gubernur DIY Bidang Sosial, Budaya dan Kemasyarakatan. Adapun Materi dapat diakses melalui: <a href="http://bit.ly/KonsultasiPublikRKPD2020">http://bit.ly/KonsultasiPublikRKPD2020</a> (klik url/ pranala tersebut).</p>
